from typical90.b import solve_b


def test_solve_b():
    assert list(solve_b(0)) == []
    assert list(solve_b(1)) == []
    assert list(solve_b(2)) == ["()"]
    assert list(solve_b(3)) == []
    assert list(solve_b(4)) == ["(())", "()()"]
    assert list(solve_b(6)) == [
        "((()))",
        "(()())",
        "(())()",
        "()(())",
        "()()()",
    ]
    assert list(solve_b(10)) == [
        "((((()))))",
        "(((()())))",
        "(((())()))",
        "(((()))())",
        "(((())))()",
        "((()(())))",
        "((()()()))",
        "((()())())",
        "((()()))()",
        "((())(()))",
        "((())()())",
        "((())())()",
        "((()))(())",
        "((()))()()",
        "(()((())))",
        "(()(()()))",
        "(()(())())",
        "(()(()))()",
        "(()()(()))",
        "(()()()())",
        "(()()())()",
        "(()())(())",
        "(()())()()",
        "(())((()))",
        "(())(()())",
        "(())(())()",
        "(())()(())",
        "(())()()()",
        "()(((())))",
        "()((()()))",
        "()((())())",
        "()((()))()",
        "()(()(()))",
        "()(()()())",
        "()(()())()",
        "()(())(())",
        "()(())()()",
        "()()((()))",
        "()()(()())",
        "()()(())()",
        "()()()(())",
        "()()()()()",
    ]
