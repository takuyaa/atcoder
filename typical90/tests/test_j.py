from typical90.j import solve_j


def test_solve_j():
    assert solve_j(
        C=[1, 2, 2, 1, 2, 1, 1],
        P=[72, 78, 94, 23, 89, 40, 75],
        L=[2],
        R=[6],
    ) == [(63, 261)]

    assert solve_j(
        C=[1, 2, 2, 1, 2, 1, 1],
        P=[72, 78, 94, 23, 89, 40, 75],
        L=[1, 2, 3, 4, 5, 1, 2, 3, 1, 2],
        R=[3, 4, 5, 6, 7, 5, 6, 7, 6, 7],
    ) == [
        (72, 172),
        (23, 172),
        (23, 183),
        (63, 89),
        (115, 89),
        (95, 261),
        (63, 261),
        (138, 183),
        (135, 261),
        (138, 261),
    ]

    assert solve_j(C=[1], P=[100], L=[1, 1, 1], R=[1, 1, 1],) == [
        (100, 0),
        (100, 0),
        (100, 0),
    ]
